// Quick access section showing pinned saved searches
function QuickAccessSection({ isCollapsed }) {
  const { user } = useAuth()
  const navigate = useNavigate()
  const [pinnedSearches, setPinnedSearches] = useState([])
  const [loading, setLoading] = useState(true)

  useEffect(() => {
    if (user?.uid) {
      loadPinned()
    }
  }, [user?.uid])

  async function loadPinned() {
    try {
      const data = await savedSearches.getPinned(user.uid)
      setPinnedSearches(data.slice(0, 5)) // Show max 5
    } catch {
      // Ignore errors
    } finally {
      setLoading(false)
    }
  }

  if (!user || loading || pinnedSearches.length === 0) {
    return null
  }

  const colorClasses = {
    blue: 'bg-blue-500',
    green: 'bg-green-500',
    yellow: 'bg-yellow-500',
    red: 'bg-red-500',
    purple: 'bg-purple-500',
    cyan: 'bg-cyan-500',
    orange: 'bg-orange-500',
    pink: 'bg-pink-500',
  }

  function handleClick(search) {
    const pageInfo = SEARCHABLE_PAGES[search.page]
    if (pageInfo) {
      // Navigate to page with saved search applied via URL params
      const params = new URLSearchParams()
      params.set('saved', search.id)
      navigate(`/${search.page}?${params.toString()}`)
    }
  }

  return (
    <div className={clsx('border-t border-gray-800', isCollapsed ? 'px-2 py-2' : 'px-4 py-3')}>
      {!isCollapsed && (
        <div className="text-xs text-gray-500 mb-2 flex items-center gap-1">
          <svg className="w-3 h-3" fill="currentColor" viewBox="0 0 20 20">
            <path d="M9.049 2.927c.3-.921 1.603-.921 1.902 0l1.07 3.292a1 1 0 00.95.69h3.462c.969 0 1.371 1.24.588 1.81l-2.8 2.034a1 1 0 00-.364 1.118l1.07 3.292c.3.921-.755 1.688-1.54 1.118l-2.8-2.034a1 1 0 00-1.175 0l-2.8 2.034c-.784.57-1.838-.197-1.539-1.118l1.07-3.292a1 1 0 00-.364-1.118L2.98 8.72c-.783-.57-.38-1.81.588-1.81h3.461a1 1 0 00.951-.69l1.07-3.292z" />
          </svg>
          Quick Access
        </div>
      )}
      <div className={clsx('space-y-1', isCollapsed && 'flex flex-col items-center gap-1')}>
        {pinnedSearches.map(search => (
          <button
            key={search.id}
            onClick={() => handleClick(search)}
            className={clsx(
              'flex items-center gap-2 text-sm text-gray-400 hover:text-white transition-colors w-full',
              isCollapsed ? 'justify-center p-1.5' : 'py-1'
            )}
            title={isCollapsed ? search.name : undefined}
          >
            <span className={clsx('w-2 h-2 rounded-full flex-shrink-0', colorClasses[search.color] || 'bg-gray-500')} />
            {!isCollapsed && (
              <span className="truncate">{search.name}</span>
            )}
          </button>
        ))}
      </div>
    </div>
  )
}

